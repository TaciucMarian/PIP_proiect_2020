package proiect;
/**
 * 
 * Clasa Fruits care construieste obiecete de tipul fructe
 *
 */

public class Fruits{
	
	String fruits;
	
	/**
	 * 
	 * Constructor cu argument
	 */
	
	public Fruits(String f)
	{
		fruits=f;
	}
	
	/**
	 * 
	 * Functie preluarea fruct
	 */
	
	public String getFruits()
	{
		return fruits;
	}

}
